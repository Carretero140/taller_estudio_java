public class Pasajeros {
    
    private int cedula;
    private String nombre;
    private int edad;
    private int celular;
    private int celularFamiliar;
    
    public Pasajeros(){

    }
    
    public Pasajeros (int cedula, String nombre, int edad, int celular, int celularFamiliar){
    
        this.cedula = cedula;
        this.nombre = nombre;
        this.edad = edad;
        this.celular = celular;
        this.celularFamiliar = celularFamiliar;
    }
}
